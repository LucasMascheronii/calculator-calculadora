value1= input("please select your first value: ")
operation= input("please select your operation: + - * / : ")
value2= input("please select your second value: ") 

if operation== "+":
  result= int(value1)+int(value2)
  print (result)

if operation== "-":
  result= int(value1)-int(value2)
  print (result)

if operation=="*":
  result= int(value1)*int(value2)
  print (result)

if operation== "/":
  result= int(value1)/int(value2)  
  print(result)
